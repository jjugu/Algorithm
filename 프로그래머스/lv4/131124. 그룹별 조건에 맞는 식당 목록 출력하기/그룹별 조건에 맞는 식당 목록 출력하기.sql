SELECT A.MEMBER_NAME
     , B.REVIEW_TEXT
     , DATE_FORMAT(B.REVIEW_DATE, '%Y-%m-%d') AS REVIEW_DATE
  FROM MEMBER_PROFILE A
  LEFT JOIN REST_REVIEW B ON A.MEMBER_ID = B.MEMBER_ID
 WHERE A.MEMBER_ID IN 
       (SELECT MEMBER_ID
          FROM (SELECT MEMBER_ID
                     , COUNT(MEMBER_ID)
                     , DENSE_RANK () OVER (ORDER BY COUNT(MEMBER_ID) DESC) AS RK
                 FROM REST_REVIEW
                GROUP BY MEMBER_ID
               ) MEMBER_PROFILE
         WHERE RK = 1
       )
 ORDER BY 3 ASC, 2 ASC;